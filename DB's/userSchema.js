const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        required: true,
        enum: ['customer', 'transport_company', 'driver', 'warehouse_manager']
    }
}, { timestamps: true });

module.exports = mongoose.model('User', UserSchema);
